
function showUserGoal(){
	if($('#userGoal').css('display') == 'none'){
	$('#userGoal').show();
	}
  }
function hideUserGoal(){
	if($('#userGoal').css('display') != 'none'){
		$('#userGoal').hide();
		}			
}
function hideUserInfo(){
	if($('#userInfo').css('display') != 'none'){
		$('#userInfo').hide();
		}			
}

		